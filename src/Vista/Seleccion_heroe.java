    public void Guardar(){
        int pro = Integer.parseInt(txtid.getText());
        String nom = txtnombre.getText();
        int vid = Integer.parseInt(txtvida.getText());
        
        Superheroe p = new Superheroe();
        
        p.setId_super(pro);
        p.setNom_super(nom);
        p.setSalud(vid);
        
        D_Superheroe obj = new D_Superheroe();
        int respuesta = obj.Guardar(p);
        if (respuesta>0){
            JOptionPane.showMessageDialog(null,"Guardado");
        }
    }

